day=int(input())

is_monday=(day==1)
is_tuesday=(day==2)
is_wednesday=(day==3)
is_thrusday=(day==4)
is_friday=(day==5)
is_saturday=(day==6)
is_sunday=(day==7)

if is_monday:
    print("Monday")
elif  is_tuesday:
    print("Tuesday")
elif  is_wednesday:
    print("Wednesday")
elif is_thrusday:
    print("Thursday")
elif  is_friday:
    print("Friday")
elif   is_saturday:
    print("Saturday")
elif   is_sunday:
    print("Sunday")