A=int(input())

no_2000s=(A//2000)
b=A-(no_2000s*2000)
no_500s=(b//500)
c=b-(no_500s*500)
no_200s=(c//200)
d=c-(no_200s*200)
no_50s=(d//50)
e=d-(no_50s*50)
no_20s=(e//20)
f=e-(no_20s*20)
no_5s=(f//5)
g=f-(no_5s*5)
no_2s=(g//2)
h=g-(no_2s*2)
no_1s=(h//1)

print("2000:"+ str(no_2000s)+" 500:"+ str(no_500s)+" 200:"+ str(no_200s)+" 50:"+ str(no_50s)+" 20:"+ str(no_20s)+" 5:"+ str(no_5s)+" 2:"+ str(no_2s)+" 1:"+ str(no_1s))
