N=int(input())

no_1000s=(N//1000)
A=N-(no_1000s*1000)
no_500s=(A//500)
B=A-(no_500s*500)
no_100s=(B//100)
C=B-(no_100s*100)
no_50s=(C//50)
D=C-(no_50s*50)
no_20s=(D//20)
E=D-(no_20s*20)
no_5s=(E//5)
D=E-(no_5s*5)
no_1s=(D//1)

print("1000:"+str(no_1000s))
print("500:"+str(no_500s))
print("100:"+str(no_100s))
print("50:"+str(no_50s))
print("20:"+str(no_20s))
print("5:"+str(no_5s))
print("1:"+str(no_1s))